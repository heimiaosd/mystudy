#include<stdio.h>

//输入一行有几个灯 a  共有几行 b 计算灯个数

int main()
{
    int a,b;
    printf("输入一行有几个灯 共几行\n");
    while(~scanf("%d%d",&a,&b))
    {
        printf("一行有 %d个灯\n共 %d行\n一共%d个灯\n",a,b,a*b);
    }
    
    return 0;
}
