#include<stdio.h>

//输入加速度 起飞速度 输出跑道长度 公式 长度 = (v*v)/(2*a)

int main()
{
    int a,v;
    printf("输入加速度 起飞速度\n");
    while(~scanf("%d%d",&a,&v))
    {
        printf("最短跑道长度 = %d\n",(v*v)/(2*a));
    }
    return 0;
}
