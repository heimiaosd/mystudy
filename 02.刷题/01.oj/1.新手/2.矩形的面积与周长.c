#include<stdio.h>

//对于一个给定长和宽的矩形 输出它的面积和周长

int main()
{
    int w,h;
    printf("输入矩形的长和宽\n");
    while(~scanf("%d%d",&w,&h)) 
    {
        printf("面积=%d\n周长=%d\n",w*h,(w+h)*2);
    }
    return 0;
}
