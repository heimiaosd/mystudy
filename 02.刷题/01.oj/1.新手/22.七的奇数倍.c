#include<stdio.h>

//如果一个数既是 7 的倍数又不能被 2 整除我们称之为 7 的奇数倍 输入一个数 n 判断它是否是 7的奇数倍 是 YES 不是 NO

int main()
{
    int n;
    printf("输入一个数\n");
    while(~scanf("%d",&n))
    {
        printf("是7的奇数倍吗 %s\n",n%7==0&&n%2!=0?"YES":"NO");
    }

    return 0;
}
