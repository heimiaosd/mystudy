//给定三个小于100的正整数 求他们的乘积

#include<stdio.h>

int main()
{
    int a,b,c; 
    printf("输入3个数用空格隔开:\n");
    while(~scanf("%d%d%d",&a,&b,&c))
    {
        printf("%d*%d*%d = %d\n",a,b,c,a*b*c);
    }
    return 0;
}
