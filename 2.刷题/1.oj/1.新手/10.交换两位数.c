#include<stdio.h>

//读入一个两位的个位不为零的正整数 将它的十位数和个位数调换后输出

int main()
{
    int n;
    printf("输入一个两位整数\n");
    while(~scanf("%d",&n))
    {
        printf("原数字 = %d\n翻转后 = %d\n",n,n%10*10+(int)n/10);
    }
    return 0;
}
