#include<stdio.h>

//输入当前的空气湿度 h 当大于55.4 就很有可能下雨 判断今天是否会下雨 

int main()
{
    double h;
    printf("输入今天的空气湿度\n");
    while(~scanf("%lf",&h))
    {
        printf("今天会下雨吗 %s\n",h>55.4?"YES":"NO");
    }
    return 0;
}
