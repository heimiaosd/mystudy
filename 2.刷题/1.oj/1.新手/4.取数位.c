#include<stdio.h>

//给定一个两位数的整数 分别输出它的十位数和个位数

int main()
{
    int a;
    printf("输入一个十位数\n");
    while(~scanf("%d",&a))
    {
        printf("原数字是 %d\n十位数是 %d\n个位数是 %d\n",a,(int)a/10,a%10);
    }
    return 0;
}
