#include<stdio.h>

//读入一个实数表示的摄氏温度c 将它转换成华氏度 F 并输入 转换公式如下 F = 1.8 * C + 32 结果保留两位小数

int main()
{
    double c;
    printf("输入一个摄氏度:\n"); 
    while(~scanf("%lf",&c))
    {
        printf("转换后 %0.2lf\n",1.8*c+32);
    }
    return 0;
}
